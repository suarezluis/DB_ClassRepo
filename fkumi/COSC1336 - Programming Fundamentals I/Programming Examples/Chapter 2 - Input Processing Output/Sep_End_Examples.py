# This program demonstrates how to use the 'end' and 'sep'

value1 = 10.0
value2 = 20.5
value3 = 30.6

print('1. The values are', value1, value2, value3, sep='')
print('2. The values are', value1, value2, value3, sep='*', end='')
print('3. The values are', value1, value2, value3, sep='@%&')
print('4. The values are', value1, value2, value3, end='\n')
print('5. The values are', value1, value2, value3, sep='*', end='???')
